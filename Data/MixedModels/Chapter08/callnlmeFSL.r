funreg <-
function(a, xi)
{
	n = length(xi)
	fret = matrix(ncol = 5, nrow = n)
	ex = exp(a[2] - a[3] * xi - a[4] * xi^2)
	fret[, 5] = a[1]/(1 + ex)
	fret[, 1] = 1/(1 + ex)
	fret[, 2] = ( - a[1] * ex)/(1 + ex)^2
	fret[, 3] = (a[1] * ex)/(1 + ex)^2 * xi
	fret[, 4] = (a[1] * ex)/(1 + ex)^2 * xi^2
	return(fret)
}
nlmeFSL <-
function(dat, fFSL, beta, Uuw, s2 = 1, D, Maxiter = 20, eps = 1.0000000000000001e-005, pr = 1)
{
	id <- dat[, 1]
	# Let c be an m-dimesional vector and M be a mxn matrix. In S-Plus all three operations are equivalent:
	# c*M, diag(c,m,m)%*%M, (c%*%t(rep(1,n)))*M
	m <- k <- length(beta)
	kk <- (k * (k + 1))/2
	em <- rep(1, m)
	Dk <- dupp(k)
	Im <- diag(rep(1, m), m, m)
	y <- dat[, 2]
	x <- dat[, 3]
	Nt <- length(id)
	U <- Uuw[, 1:m]
	w <- Uuw[, (m + 1)]
	idu <- unique(id)
	N <- length(idu)
	Dc <- t(chol(D))
	delta <- t(Dk) %*% as.vector(Dc)
	tau <- c(s2, beta, delta)
	tpar <- length(tau)
	SS <- nrow(U)
	for(it in 1:Maxiter) {
		iterdone = it
		loglik <- -0.5 * Nt * log(s2) - (Nt + N * m)/2 * log(8 * atan(1))
		grad <- rep(0, tpar)
		HH <- matrix(0, tpar, tpar)
		for(idi in idu) {
			yi <- y[id == idi]
			xi <- x[id == idi]
			ni <- length(xi)
			ses <- ses2 <- 0
			der <- rep(0, tpar)
			for(s in 1:SS) {
				us <- U[s,  ]
				pl <- kronecker(t(us), Im) %*% Dk
				a <- beta + pl %*% delta
				ff <- fFSL(a, xi)
				res <- yi - ff[, m + 1]
				dee <- sum(res^2)
				es <- exp( - dee/s2)
				ses <- ses + w[s] * es
				ses2 <- ses2 + w[s] * es * dee
				dd <- (t(ff[, 1:m]) %*% res) * w[s] * es
				der[2:(m + 1)] <- der[2:(m + 1)] + dd
				der[(m + 2):tpar] <- der[(m + 2):tpar] + t(pl) %*% dd
			}
			der[1] <- (-0.5 * ni)/s2 + 0.5/s2^2/ses * ses2
			der[2:tpar] <- der[2:tpar]/ses/s2
			grad <- grad + der
			HH <- HH + der %*% t(der)
			li <- log(ses)
			loglik <- loglik + li
		}
		iHH <- solve(HH)
		dd <- iHH %*% grad
		tau <- tau + dd
		s2 <- tau[1]
		beta <- tau[2:(m + 1)]
		delta <- tau[(m + 2):tpar]
		if(max(abs(dd)) < eps)
			return(list(iterdone, tau, iHH))
		if(pr) 
		{
			print(paste("iteration=",it,", loglik=",loglik,", grad=",sqrt(sum(grad^2))))
			print("beta:");print(beta);
			print("s2 and Chol D (tau):");print(c(s2,delta))
		}
	}
	return(list(iterdone, tau, iHH))
}
gauher <-
function(n, EPS = 2.9999999999999998e-014, PIM4 = 0.75112554446494295, MAXIT = 100)
{
	K = n
	x <- w <- rep(0, K)
	m = (K + 1)/2
	for(i in 1:m) {
		if(i == 1) {
			z = sqrt(2 * K + 1) - 1.85575 * (2 * K + 1)^(-0.16667000000000001)
		}
		else if(i == 2) {
			z = z - (1.1399999999999999 * K^0.42599999999999999)/z
		}
		else if(i == 3) {
			z = 1.8600000000000001 * z - 0.85999999999999999 * x[1]
		}
		else if(i == 4) {
			z = 1.9099999999999999 * z - 0.91000000000000003 * x[2]
		}
		else {
			z = 2. * z - x[i - 2]
		}
		for(its in 1:MAXIT) {
			p1 = PIM4
			p2 = 0.
			for(j in 1:K) {
				p3 = p2
				p2 = p1
				p1 = z * sqrt(2./j) * p2 - sqrt((j - 1)/j) * p3
			}
			pp = sqrt(2 * K) * p2
			z1 = z
			z = z1 - p1/pp
			if(abs(z - z1) <= EPS)
				break
		}
		x[i] = z
		x[K + 1 - i] =  - z
		w[i] = 2./(pp * pp)
		w[K + 1 - i] = w[i]
	}
	return(cbind(x, w))
}
